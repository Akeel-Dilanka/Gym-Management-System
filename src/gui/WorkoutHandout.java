    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String memberId = jTextField10.getText();
        String set = jTextField11.getText();
        String workout = jLabel25.getText();

        if (memberId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Membership Id", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (workout.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please select Workout", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (set.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Set count", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

            boolean isFound = false;

            for (int i = 0; i < dtm.getRowCount(); i++) {
                String Checkworkout = jTable1.getValueAt(i, 0).toString();

                if (Checkworkout.equals(workout)) {
                    isFound = true;
                    break;
                }
            }

            if (isFound) {
                JOptionPane.showMessageDialog(this, "This Workout is already added", "Warning", JOptionPane.WARNING_MESSAGE);

                resetWorkoutFields();
 

            } else {
                Vector v = new Vector();
                v.add(workout);
                v.add(set);
                v.add(jTextField12.getText());
                v.add(jTextField13.getText());
                v.add(jTextField14.getText());
                v.add(jTextField15.getText());
                v.add(jTextField16.getText());
                v.add(jTextField17.getText());
                v.add(jTextField18.getText());
                v.add(jTextField19.getText());
                
                dtm.addRow(v);
                resetWorkoutFields();

                JOptionPane.showMessageDialog(this, "Workout added to the Handout", "Success", JOptionPane.WARNING_MESSAGE);
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

