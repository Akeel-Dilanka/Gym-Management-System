    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String category = jComboBox1.getSelectedItem().toString();
        String dpw = jTextField20.getText();
        String memberId = jTextField10.getText();
        Date validity = jDateChooser3.getDate();
        String diet = jLabel18.getText();
        String supplement = jLabel19.getText();
        
        if (memberId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Membership Id", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (jTable1.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Please add Workout Plans", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (category.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select Workout Category", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (validity == null) {
            JOptionPane.showMessageDialog(this, "Please enter Workout Validity End Date", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (validity.before(new Date())) {
            JOptionPane.showMessageDialog(this, "Invalid Workout Validity End Date", "Warning", JOptionPane.WARNING_MESSAGE);    
        } else if (dpw.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Membership Id", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (diet.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please select Diet Plan", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (supplement.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select Supplement & Nutrients", "Warning", JOptionPane.WARNING_MESSAGE);    
        } else {

            try {
                ResultSet rs1 = MySQL.search("SELECT `id` FROM `workout_category` WHERE `name` = '" + category + "'");
                rs1.next();
                String CategoryId = rs1.getString("id");
                
                String validDate = sdf.format(validity);

                ResultSet rs2 = MySQL.search("SELECT * FROM `diet_plan` WHERE `name` = '" + diet + "' ");
                rs2.next();
                String dietPlanId = rs2.getString("id");
                String dietPlan = rs2.getString("Description");
                
                ResultSet rs3 = MySQL.search("SELECT `id` FROM `supplement & nutrients` WHERE `name` = '" + supplement + "' ");
                rs3.next();
                String supplementId = rs3.getString("id");
                
                ResultSet rs4 = MySQL.search("SELECT `id` FROM `member` WHERE `membership_id` = '" + memberId + "' ");
                rs4.next();
                String mId = rs4.getString("id");
                
                
                //Jasper Report
                InputStream Stream = getClass().getResourceAsStream("/reports/GymWorkoutHandout.jrxml");
                JasperReport jr = JasperCompileManager.compileReport(Stream);
                HashMap parameters = new HashMap();
                Vector v = new Vector();
                
                Date d = new Date();
                SimpleDateFormat sd_time = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss a");
                
                String age = jLabel17.getText();
                String gender = jLabel16.getText();
                String mass = jLabel36.getText();
                String height = jLabel37.getText();
                String bmi = jLabel38.getText();
                String facility = jLabel24.getText();
                
                parameters.put("Parameter1", sd_time.format(d));
                parameters.put("Parameter2", memberId);
                parameters.put("Parameter3", age);
                parameters.put("Parameter4", gender);
                parameters.put("Parameter5", mass);
                parameters.put("Parameter6", height);
                parameters.put("Parameter7", bmi);
                parameters.put("Parameter8", category);
                parameters.put("Parameter9", validDate);
                parameters.put("Parameter10", dpw);
                parameters.put("Parameter11", facility);
                parameters.put("Parameter12", supplement);
                parameters.put("Parameter13", dietPlan);
                //Jasper Report

                for (int i = 0; i < jTable1.getRowCount(); i++) {

                    String workout = jTable1.getValueAt(i, 0).toString();
                    String set = jTable1.getValueAt(i, 1).toString();
                    String reps = jTable1.getValueAt(i, 2).toString();
                    String set1 = jTable1.getValueAt(i, 3).toString();
                    String set2 = jTable1.getValueAt(i, 4).toString();
                    String set3 = jTable1.getValueAt(i, 5).toString();
                    String set4 = jTable1.getValueAt(i, 6).toString();
                    String set5 = jTable1.getValueAt(i, 7).toString();
                    String set6 = jTable1.getValueAt(i, 8).toString();
                    String rest = jTable1.getValueAt(i, 9).toString();

                    ResultSet rs = MySQL.search("SELECT `id` FROM `workout` WHERE `name` = '" + workout + "'");
                    rs.next();
                    String workoutId = rs.getString("id");
                    
                        MySQL.iud("INSERT INTO `workout_handout`(`workout_category_id`,`valid_end_date`,`no_days_week`,`workout_id`,`SETS`,`REPS`,`set1`,`set2`,`set3`,`set4`,`set5`,`set6`,`rest_time`,`diet_plan_id`,`supplement & nutrients_id`,`member_id`,`user_id`) VALUES ('" + CategoryId + "','" + validDate + "','" + dpw + "','" + workoutId + "','" + set + "','" + reps + "','" + set1 + "','" + set2 + "','" + set3 + "','" + set4 + "','" + set5 + "','" + set6 + "','" + rest + "','" + dietPlanId + "','" + supplementId + "','" + mId + "','" + signIn.userId + "')");
                    
                        
                //Jasper Report
                    
                v.add(new BeanWorkoutHandout(workout, set, reps, set1, set2, set3, set4, set5, set6, rest));
                
                //Jasper Report
                }

                resetFields();
                resetWorkoutFields();

                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(0);

                JOptionPane.showMessageDialog(this, "New Workout Handout Created", "Success", JOptionPane.INFORMATION_MESSAGE);
                
                //Jasper Report
                JRBeanCollectionDataSource datasource = new JRBeanCollectionDataSource(v);
                JasperPrint jp = JasperFillManager.fillReport(jr, parameters, datasource);
                JasperViewer jv = new JasperViewer(jp, false);
                jv.setVisible(true);
                //Jasper Report
                
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

