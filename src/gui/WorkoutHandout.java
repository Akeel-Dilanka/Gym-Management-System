    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String memberId = jTextField10.getText();
        
        if (memberId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Membership Id", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("[1-9][0-9]*").matcher(memberId).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid Membership ID", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet rs1 = MySQL.search("SELECT `physique_id`,`membership_plan_id` FROM `member` WHERE `membership_id` = '" + memberId + "'");
                if (rs1.next()) {
                
                String physiqueId = rs1.getString("physique_id");
                String membershipPlanId = rs1.getString("membership_plan_id");

                ResultSet rs2 = MySQL.search("SELECT physique.age, physique.mass, physique.height, physique.BMI, gender.name FROM physique INNER JOIN gender ON physique.gender_id = gender.id WHERE physique.id = '" + physiqueId + "'");
                rs2.next();
                String age = rs2.getString("physique.age");
                String mass = rs2.getString("physique.mass");
                String height = rs2.getString("physique.height");
                String BMI = rs2.getString("physique.BMI");
                String gender = rs2.getString("gender.name");
                
                ResultSet rs3 = MySQL.search("SELECT `membership_card` FROM `membership_plan` WHERE `id` = '" + membershipPlanId + "'");
                rs3.next();
                String membershipCard = rs3.getString("membership_card");
                
                jLabel17.setText(age);
                jLabel16.setText(gender);
                jLabel36.setText(mass);
                jLabel37.setText(height);
                jLabel38.setText(BMI);
                jLabel22.setText(membershipCard);
                
                String bmiNo = jLabel38.getText();
                double range = Double.parseDouble(bmiNo);
                if (range < 24) {
                    jLabel40.setText("The member is underweight");
                    jLabel40.setForeground(java.awt.Color.RED);
                } else if (range < 29) {
                    jLabel40.setText("The member is healthy");
                    jLabel40.setForeground(java.awt.Color.GREEN);
                } else if (range < 34) {
                    jLabel40.setText("The member is overweight");
                    jLabel40.setForeground(java.awt.Color.RED);
                } else {
                    jLabel40.setText("The member is OBESE");
                    jLabel40.setForeground(java.awt.Color.RED);
                }
                
                
                String Card = jLabel22.getText();
                if ("GOLD Card".equals(Card)) {
                    jLabel24.setText("All Weight Training & Limited Cardio Training (Only - 15 min)");
                    jLabel24.setForeground(java.awt.Color.YELLOW);
                } else if ("PLATINUM Card".equals(Card)) {
                    jLabel24.setText("All Weight Training & Unlimited Cardio Training");
                    jLabel24.setForeground(java.awt.Color.LIGHT_GRAY);
                }

                }else{
                    JOptionPane.showMessageDialog(this, "Invalid Membership ID", "Warning", JOptionPane.WARNING_MESSAGE);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

