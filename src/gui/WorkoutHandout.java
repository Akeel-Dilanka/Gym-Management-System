    public void loadWorkoutCategory() {

        try {

            ResultSet rs = MySQL.search("SELECT * FROM `workout_category` ORDER BY `workout_category`.name ASC");

            Vector v = new Vector();
            v.add("Select");

            while (rs.next()) {
                v.add(rs.getString("name"));
                DefaultComboBoxModel dcm = new DefaultComboBoxModel(v);
                jComboBox1.setModel(dcm);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
