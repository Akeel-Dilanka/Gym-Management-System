    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String uid = jLabel36.getText();
        String paymentid = jLabel37.getText();
        
            SimpleDateFormat sdfh = new SimpleDateFormat("yyyy-MM-dd");
            String dNow = sdfh.format(new Date());
            
            try {

                MySQL.iud("INSERT INTO `salary`(`date`,`payment_plan_id`,`user_id`) VALUES ('" + dNow + "','" + paymentid + "','" + uid + "')");

                //Jasper Report
                InputStream Stream = getClass().getResourceAsStream("/reports/GymSalarySlip.jrxml");
                JasperReport jr = JasperCompileManager.compileReport(Stream);
                HashMap parameters = new HashMap();
                Vector v = new Vector();
                
                Date d = new Date();
                SimpleDateFormat sd_time = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss a");
                
                String epname = jLabel17.getText();
                String role = jLabel16.getText();
                String salary = jLabel38.getText();
                String usid = String.valueOf(signIn.userId);
                
                ResultSet rs5 = MySQL.search("SELECT `contact_number`,`last_name` FROM `user` WHERE `first_name` = '" + epname + "'");
                rs5.next();
                String lname = rs5.getString("last_name");
                String contact = rs5.getString("contact_number");
                
                parameters.put("Parameter1", sd_time.format(d));
                parameters.put("Parameter2", epname+" "+lname);
                parameters.put("Parameter3", contact);
                parameters.put("Parameter4", role);
                parameters.put("Parameter5", usid);
                parameters.put("Parameter6", uid);
                
                v.add(new BeanPaySheet(dNow, salary));
                //Jasper Report
                
                loadSalary();
                resetFields();
                this.resize(1400, 900);

                JOptionPane.showMessageDialog(this, "New Salary Slip Created", "Success", JOptionPane.INFORMATION_MESSAGE);
                
                //Jasper Report
                JRBeanCollectionDataSource datasource = new JRBeanCollectionDataSource(v);
                JasperPrint jp = JasperFillManager.fillReport(jr, parameters, datasource);
                JasperViewer jv = new JasperViewer(jp, false);
                jv.setVisible(true);
                //Jasper Report

            } catch (Exception e) {
                e.printStackTrace();
            }
    }//GEN-LAST:event_jButton1ActionPerformed


