    public void loadPaymentPlan() {

        try {

            ResultSet rs = MySQL.search("SELECT payment_plan.id, user_roles.name, payment_plan.monthly_payment FROM payment_plan INNER JOIN user_roles ON payment_plan.user_roles_id = user_roles.id ORDER BY user_roles.id ASC");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("payment_plan.id"));
                v.add(rs.getString("user_roles.name"));
                v.add(rs.getString("payment_plan.monthly_payment"));
                dtm.addRow(v);
            }

            jTable1.setModel(dtm);
            this.resize(1400, 900);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    public void loadSalary() {

        try {

            ResultSet rs = MySQL.search("SELECT salary.id, user.first_name, payment_plan.monthly_payment, salary.date FROM salary INNER JOIN payment_plan ON salary.payment_plan_id = payment_plan.id INNER JOIN user ON salary.user_id = user.id ORDER BY salary.id ASC");
            DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("salary.id"));
                v.add(rs.getString("user.first_name"));
                v.add(rs.getString("payment_plan.monthly_payment"));
                v.add(rs.getString("salary.date"));
                dtm.addRow(v);
            }

            jTable2.setModel(dtm);
            this.resize(1400, 900);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    public void loadUserRoles() {

        try {

            ResultSet rs = MySQL.search("SELECT `name` FROM `user_roles` ORDER BY `user_roles`.`name` ASC");

            Vector v = new Vector();
            v.add("Select");

            while (rs.next()) {
                v.add(rs.getString("name"));
            }
            jComboBox1.setModel(new DefaultComboBoxModel(v));

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
