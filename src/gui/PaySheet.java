    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String userrole = jComboBox1.getSelectedItem().toString();
        String monthlypayment = jTextField3.getText();

        if (userrole.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select User Role", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("(0)|([1-9][0-9]*)|([0][.]([0]+[1-9]+)|([0-9]+))|([1-9][0-9]*[.][0-9]+)").matcher(monthlypayment).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid Monthly Payment", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {
                ResultSet rs = MySQL.search("SELECT `id` FROM `user_roles` WHERE `name` = '" + userrole + "'");
                rs.next();
                String roleid = rs.getString("id");
                
                MySQL.iud("UPDATE `payment_plan` SET `monthly_payment` = '" + monthlypayment + "' WHERE `user_roles_id` = '" + roleid + "'");
                
                loadPaymentPlan();
                resetFields();
                this.resize(1400, 900);
                
            } catch (Exception e) {
            e.printStackTrace();
        }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String useremail = jTextField10.getText();

        if (useremail.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter User Email", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("^[a-z0-9._%+-]+@[a-z0-9.-]+\\.[a-z]{2,6}$").matcher(useremail).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid email", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet rs1 = MySQL.search("SELECT user.first_name, user.id, user_roles.name, payment_plan.id, payment_plan.monthly_payment FROM payment_plan INNER JOIN user_roles ON payment_plan.user_roles_id = user_roles.id INNER JOIN user ON user.user_roles_id = user_roles.id WHERE `user`.`user_email` = '" + useremail + "' ORDER BY user_roles.id ASC");
                rs1.next();
                String username = rs1.getString("user.first_name");
                String userrole = rs1.getString("user_roles.name");
                String paymentid = rs1.getString("payment_plan.id");
                String monthlypayment = rs1.getString("payment_plan.monthly_payment");
                String uid = rs1.getString("user.id");

                    jLabel17.setText(username);
                    jLabel16.setText(userrole);
                    jLabel36.setText(uid);
                    jLabel37.setText(paymentid);
                    jLabel38.setText(monthlypayment);

                    
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

